router.patch('/:id/status', authenticate, authorize([Role.ADMIN, Role.LIBRARIAN]), async (req, res) => {
  try {
    const authorId = Number(req.params.id);
    const { status } = req.body;

    if (!status || !['ACTIVE', 'DISABLED'].includes(status)) {
      return res.status(400).json({ message: '无效的状态值' });
    }

    const author = await prisma.author.update({
      where: { id: authorId },
      data: { status: status as AuthorStatus },
    });

    res.json({
      message: status === 'DISABLED' ? '作者已停用' : '作者已启用',
      author,
    });
  } catch (_error) {
    res.status(400).json({ message: '更新作者状态失败', error: (_error as any).message });
  }
});

router.delete('/:id', authenticate, authorize([Role.ADMIN]), async (req, res) => {
  try {
    const authorId = Number(req.params.id);

    const bookAuthorCount = await prisma.bookAuthor.count({ where: { authorId } });
    if (bookAuthorCount > 0) {
      return res.status(400).json({
        message: '该作者已关联图书，请使用停用功能而非删除',
      });
    }

    await prisma.author.delete({ where: { id: authorId } });
    res.json({ message: '作者已删除' });
  } catch (_error) {
    res.status(400).json({ message: '删除作者失败', error: (_error as any).message });
  }
});


router.get('/', async (req, res) => {
  try {
    const { pinyinInitial, nationality, search, status, page = 1, pageSize = 20 } = req.query;

    const where: any = {};

    if (pinyinInitial) {
      where.pinyinInitial = Array.isArray(pinyinInitial)
        ? { in: pinyinInitial.map(String) }
        : String(pinyinInitial);
    }

    if (nationality) {
      where.nationality = Array.isArray(nationality)
        ? { in: nationality.map(String) }
        : String(nationality);
    }

    if (status) {
      where.status = String(status) as AuthorStatus;
    }

    if (search) {
      where.OR = [
        { name: { contains: String(search) } },
        { biography: { contains: String(search) } },
        { representativeWorks: { contains: String(search) } },
      ];
    }

    const skip = (Number(page) - 1) * Number(pageSize);
    const take = Number(pageSize);

    const [authors, total] = await Promise.all([
      prisma.author.findMany({
        where,
        skip,
        take,
        orderBy: [{ pinyinInitial: 'asc' }, { name: 'asc' }],
        include: {
          _count: {
            select: { bookAuthors: true },
          },
        },
      }),
      prisma.author.count({ where }),
    ]);

    const authorsWithStats = authors.map((author) => ({
      ...author,
      bookCount: author._count.bookAuthors,
    }));

    res.json({
      data: authorsWithStats,
      total,
      page: Number(page),
      pageSize: Number(pageSize),
    });
  } catch (_error) {
    res.status(400).json({ message: 'Failed to get authors', error: (_error as any).message });
  }
});

